#include "libft.h"

void	ft_putnbr(int nb)
{
	char	c;

	c = nb % 10 + '0';
	if (nb == -2147483648)
	{
		ft_putnbr(nb / 10);
		write(1, "8", 1);
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(nb * -1);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		write(1, &c, 1);
	}
	else
		write(1, &c, 1);
}
